package com.example.wangning.canvas.chart.line;

/**
 * 坐标
 *
 * @author wangning
 * @version 1.0 2017-08-16
 * @since JDK 1.8
 */
public class Coordinate {
    private int x;
    private float y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
